constexpr int MaxCities = 100;
constexpr Price MaxPrice = std::numeric_limits<Price>::max();

struct FlightPrice
{
    CityId city;
    Price price;
};

struct CityInfo
{
    //std::vector<FlightPrice> outFlights;
    std::vector<FlightPrice> inFlights;
};


std::array<CityInfo, MaxCities> flights = {};
std::array<bool, MaxCities> visited;
CityId src;
CityId dst;
int maxStops;
Price result = 0;


void Walk(const CityId toCity, const int stopsLeft, const Price curPrice)
{
    if (toCity == src) {
        if (curPrice < result)
            result = std::min(result, curPrice);
        return;
    }

    if (stopsLeft <= 0)
        return;

    const auto& info = flights[toCity];
    for (const FlightPrice& flight : info.inFlights) {
        if (visited[flight.city])
            continue;

        const auto nextPrice = curPrice + flight.price;
        if (nextPrice > result)
            continue;

        visited[flight.city] = true;
        Walk(flight.city, stopsLeft - 1, nextPrice);
        visited[flight.city] = false;
    }
}

int FindCheapestPriceImpl(const std::vector<std::vector<int>>& _flights, CityId _src, CityId _dst, int _maxStops)
{
    flights = {};
    visited.fill(false);
    src = _src;
    dst = _dst;
    maxStops = _maxStops;
    result = MaxPrice;

    for (const auto& desc : _flights) {
        const auto from = desc[0];
        const auto to = desc[1];
        const auto price = desc[2];

        //flights[from].outFlights.emplace_back(to, price);
        flights[to].inFlights.emplace_back(from, price);
    }


    visited[dst] = true;
    Walk(dst, _maxStops+1, 0);

    return result == MaxPrice ? -1 : result;
}

